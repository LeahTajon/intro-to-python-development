# Looping through a list
colors = ['red', 'blue', 'green', 'yellow']

for i in colors:
    print(i)

# Looping though numbers
print()
for j in range(1, 9):
    print(j)

# Looping though numbers - counting by 2
print()
for k in range(2, 21, 2):
    print(k)
